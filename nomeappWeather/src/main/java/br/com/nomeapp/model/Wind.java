package br.com.nomeapp.model;

public class Wind {

	private float speed;
	private float deg;

	public Wind() {
		super();
	}

	public float getSpeed() {
		return speed;
	}

	public void setSpeed(final float speed) {
		this.speed = speed;
	}

	public float getDeg() {
		return deg;
	}

	public void setDeg(final float deg) {
		this.deg = deg;
	}
}
